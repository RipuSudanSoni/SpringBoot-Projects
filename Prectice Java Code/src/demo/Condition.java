package demo;

public class Condition {

//	WhileLoopExample
	    public static void main(String[] args) {
	        // Initialize the counter variable
	        int i = 1;

	        // While loop runs as long as i is less than or equal to 5
	        while (i <= 5) {
	            System.out.println("Number: " + i);

	            // Increment the counter
	            i++;
	        }
	    }
	}
